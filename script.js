let spinInterval = null;

function startSpinSound() {
    if (!soundEnabled || !audioContext) return;
    
    let delay = 50;
    const maxDelay = 300;
    const increment = 5;
    
    function tick() {
        playSound('spin');
        delay = Math.min(delay + increment, maxDelay);
        spinInterval = setTimeout(tick, delay);
    }
    
    tick();
}

function stopSpinSound() {
    if (spinInterval) {
        clearTimeout(spinInterval);
        spinInterval = null;
    }
}

